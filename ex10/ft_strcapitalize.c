char	*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int		i;

	i = 0;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
			i++;
		}
		else
			while ((str[i] >= '0' && str[i] <= '9')
					|| (str[i] >= 'a' && str[i] <= 'z'))
				i++;
		while (str[i] >= 'a' && str[i] <= 'z')
			i++;
		i++;
	}
	return (str);
}
