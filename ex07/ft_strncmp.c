int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int		i;
	int					diff;

	i = 0;
	diff = 0;
	if (s1[i] == '\0' && s2[i] == '\0')
		return (0);
	while ((s1[i] == s2[i] && n > i) || (s1[i] == '\0' && s2[i] == '\0'))
		i++;
	if (s1[i] != s2[i])
		diff = s1[i] - s2[i];
	return (diff);
}
