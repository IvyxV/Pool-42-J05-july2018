char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (dest[i - 1] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	return (dest);
}
